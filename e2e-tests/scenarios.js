'use strict';

/* https://github.com/angular/protractor/blob/master/docs/toc.md */

describe('my app', function() {

  browser.get('index.html');

  it('should automatically redirect to /discover when location hash/fragment is empty', function() {
    expect(browser.getLocationAbsUrl()).toMatch("/discover");
  });


  describe('discover', function() {

    beforeEach(function() {
      browser.get('index.html#/discover');
    });


    it('should render discover when user navigates to /discover', function() {
      expect(element.all(by.css('[ng-view] p')).first().getText()).
        toMatch(/partial for view 1/);
    });

  });


  describe('view2', function() {

    beforeEach(function() {
      browser.get('index.html#/view2');
    });


    it('should render view2 when user navigates to /view2', function() {
      expect(element.all(by.css('[ng-view] p')).first().getText()).
        toMatch(/partial for view 2/);
    });

  });
});
